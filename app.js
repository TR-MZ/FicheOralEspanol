function readHash() {
  const [chapterId, documentId] = window.location.hash.replace("#", "").split("/");
  if (!chapterId || chapterId === "home") {
    renderHome();
    return;
  }
  if (chapterId === "vocabulaire") {
    renderFlashcards();
    return;
  }
  if (chapterId === "repassos") {
    renderRepassos();
    return;
  }
  if (chapterId === "question-orale") {
    renderOrale();
    return;
  }
  const chapterExists = chapters.some((chapter) => chapter.id === chapterId);
  if (!chapterExists) {
    renderHome();
    return;
  }
  if (documentId) renderDocument(chapterId, documentId);
  else renderChapter(chapterId);
}

