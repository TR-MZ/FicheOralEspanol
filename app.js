function imageUrl(filename) {
  return encodeURI(filename);
}

function textFor(value, language = contentLanguage) {
  if (!value) return "";
  if (typeof value === "string") return value;
  return value[language] || value.es || value.fr || "";
}

function paragraphsFor(value, language = contentLanguage) {
  const content = textFor(value, language);
  const blocks = Array.isArray(content) ? content : String(content).split(/\n{2,}/);
  return blocks
    .filter(Boolean)
    .map((paragraph) => `<p>${paragraph}</p>`)
    .join("");
}

